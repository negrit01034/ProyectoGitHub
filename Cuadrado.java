public class Cuadrado {
    
    
   public int lado;
    public int calculararea(int lado){
        int area;
        area=lado*lado;
        return area;
    }
    
    public int calcularperimetro(int lado){
        int perimetro;
        perimetro=lado+lado+lado+lado;
        return perimetro;        
    }
    
    
    
}   


    
    
    
    
    
    
    
    
    
    
    

